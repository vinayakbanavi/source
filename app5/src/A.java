class A 
{
	public static void main(String[] args) 
	{
		// UNARY OPERATOR IS AN OPERETAR WHICH IS BRINGING ONE UNIT CHANGE IN THE VARIABLE
		int i = 1;
		System.out.println(i++); 
		System.out.println(i);  
		System.out.println(i--); 
		System.out.println(i);   
		System.out.println(i);  
		System.out.println(i); 
	}
}
